    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        // TODO add your handling code here:
          // Obtém o ID do usuário a ser editado
    int id2 = Integer.parseInt(id.getText());
    
    // Obtém os novos valores do usuário e senha dos campos de texto
    String novoUsuario = Usuario.getText();
    String novaSenha = Senha.getText();
    
    // Cria uma instância do controlador para gerenciar a edição do usuário
    GerenciamentoUsuarioController controller = new GerenciamentoUsuarioController();
    
    // Chama o método para editar o usuário no banco de dados
    if (controller.editarUsuario(id2, novoUsuario, novaSenha)) {
        // Se a edição for bem-sucedida, exiba uma mensagem de sucesso
        JOptionPane.showMessageDialog(null, "Usuário editado com sucesso!");
        // Atualize a tabela de usuários
        atualizarTabelaUsuarios();
    } else {
        // Se a edição falhar, exiba uma mensagem de erro
        JOptionPane.showMessageDialog(null, "Erro ao editar usuário. Verifique os dados e tente novamente.");
    }
    atualizarTabelaUsuarios();
    }//GEN-LAST:event_EditarActionPerformed

