    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // Configuração da conexão com o banco de dados
        String url = "jdbc:mysql://localhost:3306/sistemarestaurante";
        String usuario = "root";
        String senha = "";

        // Consulta SQL para recuperar os usuários
        String sql = "SELECT * FROM usuarios";

        // Criação do modelo de tabela
        DefaultTableModel modeloTabela = new DefaultTableModel();

        // Configuração das colunas
        modeloTabela.addColumn("ID");
        modeloTabela.addColumn("Usuário");
        modeloTabela.addColumn("Senha");

        try {
            // Conexão com o banco de dados
            Connection conexao = DriverManager.getConnection(url, usuario, senha);
            Statement statement = conexao.createStatement();

            // Execução da consulta
            ResultSet resultSet = statement.executeQuery(sql);

            // Preenchimento do modelo de tabela com os dados dos usuários
            while (resultSet.next()) {
                int id = resultSet.getInt("id");
                String nome = resultSet.getString("username");
                String email = resultSet.getString("password");
                modeloTabela.addRow(new Object[]{id, nome, email});
            }

            // Fechamento dos recursos
            resultSet.close();
            statement.close();
            conexao.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }

        // Atribuição do modelo de tabela à JTable
        tabelaUsuarios.setModel(modeloTabela);
    
    }//GEN-LAST:event_formWindowOpened

    private void tabelaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaUsuariosMouseClicked
                                              
    // Obtém o modelo da tabela
    DefaultTableModel model = (DefaultTableModel) tabelaUsuarios.getModel();
    
    // Obtém o índice da linha clicada
    int rowIndex = tabelaUsuarios.getSelectedRow();
    
    // Verifica se uma linha foi selecionada
    if (rowIndex != -1) {
        // Obtém os valores das colunas da linha selecionada
        int id2 = Integer.parseInt(model.getValueAt(rowIndex, 0).toString()); // Coluna do ID (índice 0)
        String username = model.getValueAt(rowIndex, 1).toString(); // Coluna do username (índice 1)
        String password = model.getValueAt(rowIndex, 2).toString(); // Coluna da senha (índice 2)
        
        // Preenche os campos de Usuario e Senha com os valores obtidos
        id.setText(String.valueOf(id2));
        Usuario.setText(username); // Verifique se este índice está correto
        Senha.setText(password); // Verifique se este índice está correto
    }

    }//GEN-LAST:event_tabelaUsuariosMouseClicked

